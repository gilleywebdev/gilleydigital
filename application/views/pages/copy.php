<h3>Content Management System</h3>
<p><strong>What is it?</strong></p>
<p>A CMS is a system that allows you and your employees to log in and easily make text changes to the site, without needing to know any code.</p>
<p><strong>Why is it important?</strong></p>
<p>You don't want to have to call a programmer just to update a page, and I don't want to have to bill people for a bunch of 5-minute edits, so a CMS is almost always the way to go. Whether you're a restaurant owner, real estate agent, or blogger; a well-tailored CMS is going to make updating your site a very smooth experience.</p>

<h3>Search-Engine Optimization</h3>
<p><strong>What is it?</strong></p>
<p>SEO means that your site is engineered to appear near the top of the search results for certain searches related to your business, like "Best Fishmonger Portland, Maine".</p>
<p><strong>Why is it important?</strong></p>
<p>It's important because people are searching for your products and services, and you need to be at the top of that result page to get their sweet, sweet money.</p>

